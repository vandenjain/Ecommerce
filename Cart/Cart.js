function increaseQuantity(pro_id){
    var itemQuantity = document.querySelector(".quantity ." + pro_id);
    itemQuantity.innerHTML = Number(itemQuantity.innerHTML) + 1;
    
}

function decreaseQuantity(pro_id){
    var itemQuantity = document.querySelector(".quantity ." + pro_id);
    if (Number(itemQuantity.innerHTML) - 1 >= 0){
        itemQuantity.innerHTML = Number(itemQuantity.innerHTML) - 1;
    }
}
